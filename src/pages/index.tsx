import ImageCard from "@/components/card";
import Layout from "@/components/layout";
import LayoutWrapper from "@/components/layout/layoutWrapper";
import Navbar from "@/components/layout/navbar";
import NoDragImage from "@/components/noDragImage";
import { trpc } from "@/utils/trpc";
import { useSession } from "next-auth/react";
import Head from "next/head";
import Link from "next/link";
import { ReactElement, useEffect, useState } from "react";
import { NextPageWithLayout } from "./_app";

const Home: NextPageWithLayout = () => {
  const [url, setUrl] = useState("");

  const client = trpc.useContext();

  const { data: session } = useSession();

  const { data: images } = trpc.images.getAll.useQuery(
    { amount: 25 },
    {
      onSuccess: () => {
        client.user.getAll.invalidate({ id: session?.user?.id });
      },
      refetchOnWindowFocus: false,
    }
  );

  useEffect(() => {
    setUrl(window.location.origin);
  }, []);

  if (url === "") {
    return <div>Loading</div>;
  }

  return (
    <>
      <Head>
        <title>Ziimgur - Easy file hosting solution.</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="xl:columns-5 lg:columns-4 md:columns-3 sm:columns-2 space-y-4">
        {images
          ? images.map((img) => (
              <Link as={"button"} key={img.id} href={`${img.id}.${img.extension}`}>
                <ImageCard src={`${url}/api/${img.id}.${img.extension}`} />
              </Link>
            ))
          : null}
      </div>
    </>
  );
};

Home.getLayout = function getLayout(page: ReactElement) {
  return (
    <Layout>
      <Navbar />
      <LayoutWrapper>{page}</LayoutWrapper>
    </Layout>
  );
};

export default Home;
